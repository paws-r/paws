context("Make tests")

# Allow us to capture code to compare it against a function's output.
code <- function(x) {
  text <- deparse(substitute(x))
  if (text[1] == "{") {
    text <- text[-1]
    text <- text[-length(text)]
  }
  result <- paste(text, collapse = "\n")
}

# Format code so differences in style don't affect comparisons.
format_test_code <- function(code) {
  formatted <- formatR::tidy_source(text = code, output = FALSE)
  result <- strsplit(paste(formatted$text.tidy, collapse = "\n"), "\n")[[1]]
  return(result)
}

test_that("make_test no arguments", {
  api <- list(
    metadata = list(
      serviceAbbreviation = "api"
    )
  )
  operation <- list(
    name = "foo"
  )
  a <- make_test(operation, api, NULL, NA)
  e <- code({
    test_that("foo", {
      expect_error(svc$foo(), NA)
    })
  })
  actual <- format_test_code(a)
  expected <- format_test_code(e)
  expect_equal(actual, expected)
})

test_that("make_test with arguments", {
  api <- list(
    metadata = list(
      serviceAbbreviation = "api"
    )
  )
  operation <- list(
    name = "foo"
  )
  a <- make_test(operation, api, list('"bar"', 123), NA)
  e <- code({
    test_that("foo", {
      expect_error(svc$foo("bar", 123), NA)
    })
  })
  actual <- format_test_code(a)
  expected <- format_test_code(e)
  expect_equal(actual, expected)
})

test_that("make_tests", {
  api <- list(
    metadata = list(
      serviceAbbreviation = "api"
    ),

    operations = list(
      CreateFoo = list(
        name = "CreateFoo"
      ),
      DescribeFoo = list(
        name = "DescribeFoo",
        input = list(
          shape = "DescribeFooShape"
        )
      ),
      ListBar = list(
        name = "ListBar"
      ),
      ListBaz = list(
        name = "ListBaz",
        input = list(
          shape = "ListBazShape"
        )
      )
    ),

    shapes = list(
      DescribeFooShape = list(
        members = list(
          MaxResults = list()
        ),
        required = list()
      ),
      ListBazShape = list(
        members = list(
          Qux = list()
        ),
        required = list(
          "Qux"
        )
      )
    )
  )
  a <- make_tests(api)
  e <- code({
    context("api")

    svc <- paws::api()

    if (Sys.getenv("NOT_CRAN") == "true") {

    test_that("describe_foo", {
      expect_error(svc$describe_foo(), NA)
    })

    test_that("describe_foo", {
      expect_error(svc$describe_foo(MaxResults = 20), NA)
    })

    test_that("list_bar", {
      expect_error(svc$list_bar(), NA)
    })

    }
  })
  actual <- format_test_code(a)
  expected <- format_test_code(e)
  actual <- Filter(function(x) !grepl("^ *$", x), actual) # Delete blank lines.
  expect_equal(actual, expected)
})
